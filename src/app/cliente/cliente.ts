export class Cliente {

   id:number;
   
   /**
  * Atributo que modela el nombre del cliente.
  */
   nombre: string;

   /**
* Atributo que modela el apellido del cliente.
*/
   apellido; string;

   /**
* Atributo que modela el correo electronico del cliente.
*/
   correoElectronico: string;

   /**
* Atributo que modela la cedula del cliente.
*/
   cedula: string;

   /**
* Atributo que modela la direccion del cliente.
*/

   direccion: string;
   /**
* Atributo que modela el usuario del cliente.
*/
   usuario: string;

   /**
* Atributo que modela la contrasena del cliente.
*/
   contrasena: string;
}